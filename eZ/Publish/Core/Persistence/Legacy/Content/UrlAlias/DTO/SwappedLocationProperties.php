        $this->parentId = $parentId;
    }

    /** @var int */
    public $id;

    /** @var int */
    public $parentId;

    /** @var string */
    public $name;

    /** @var int */
    public $mainLanguageId;

    /** @var int */
    public $autogeneratedId;

    /** @var bool */
    public $isAlwaysAvailable;

    /**
     * Raw database records (entries).
     *
     * @var array
     */
    public $entries;
}
